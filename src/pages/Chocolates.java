package pages;

public class Chocolates {
	int totalChocolateBars;
	int treatChocolateBars;
	int friendsEatenChocolateBars;
	int coworkersEatenChocolateBars;
	
	public int getTotalChocolateBars() {
		return totalChocolateBars;
	}
	public void setTotalChocolateBars(int totalChocolateBars) {
		this.totalChocolateBars = totalChocolateBars;
	}
	public int getTreatChocolateBars() {
		return treatChocolateBars;
	}
	public void setTreatChocolateBars(int treatChocolateBars) {
		this.treatChocolateBars = treatChocolateBars;
	}
	public int getFriendsEatenChocolateBars() {
		return friendsEatenChocolateBars;
	}
	public void setFriendsEatenChocolateBars(int friendsEatenChocolateBars) {
		this.friendsEatenChocolateBars = friendsEatenChocolateBars;
	}
	public int getCoworkersEatenChocolateBars() {
		return coworkersEatenChocolateBars;
	}
	public void setCoworkersEatenChocolateBars(int coworkersEatenChocolateBars) {
		this.coworkersEatenChocolateBars = coworkersEatenChocolateBars;
	}
}
